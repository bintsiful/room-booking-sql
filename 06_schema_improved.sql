-- Improved Schema (Explicit Beds)

CREATE TABLE ROOM (
    ROOM_ID     NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ROOM_NAME   VARCHAR2(100),
    PLACE       VARCHAR2(100)
);

CREATE TABLE BED (
    BED_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ROOM_ID     NUMBER,
    BED_NUMBER  NUMBER,
    CONSTRAINT fk_bed_room FOREIGN KEY (ROOM_ID)
        REFERENCES ROOM(ROOM_ID)
);

CREATE TABLE BED_BOOKING (
    BOOKING_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY,
    BED_ID          NUMBER,
    VOLUNTEER_NAME  VARCHAR2(100),
    ARRIVAL_DATE    DATE,
    DEPARTURE_DATE  DATE,
    CONSTRAINT fk_booking_bed FOREIGN KEY (BED_ID)
        REFERENCES BED(BED_ID)
);